#include <stdio.h>
/**
 * main - Entry point
 * prints all the single digit number separated by comma
 * using only putchar function
 * Return: 0
 */
int main(void)
{
	int i;

	for (i = '0'; i <= '9'; i++)
	{
		putchar(i);
		if (i < '9')
		{
			putchar(',');
			putchar(' ');
		}
	}
	putchar('\n');
	return (0);
}
